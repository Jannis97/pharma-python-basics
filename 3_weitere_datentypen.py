
# 2. Andere Datentypen
#   2a. String
#     - Zeichenketten beliebiger Länge
#     - Beispiel: Texteingaben, Nachrichten
#     - Funktionen: str(), len(), format(), repr()
#     - Methoden: .upper(), .lower(), .split(), .replace(), .find(), .startswith(), .strip()
#   2b. Tuple
#     - Unveränderbare geordnete Sammlung
#     - Beispiel: feste Konfigurationen
#     - Funktionen: tuple(), len(), sum()
#     - Methoden: .count(), .index()
#   2c. Liste
#     - Veränderbare geordnete Sammlung
#     - Beispiel: dynamische Datenlisten
#     - Funktionen: list(), len(), sorted(), sum(), min(), max()
#     - Methoden: .append(), .extend(), .insert(), .remove(), .pop(), .sort(), .reverse(), .clear()
#   2d. Dictionary
#     - Schlüssel-Wert-Paare ohne feste Reihenfolge
#     - Beispiel: strukturierte Daten, JSON-ähnlich
#     - Funktionen: dict(), len(), zip()
#     - Methoden: .keys(), .values(), .items(), .get(), .update(), .pop(), .clear(), .setdefault()

