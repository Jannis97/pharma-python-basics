#   0.2 Grundlegende Syntax-Regeln
#     - Einrückung (Indentation) statt Klammern zur Blockbildung
#     - Zeilenenden markieren Anweisungsende (kein Semikolon nötig)
#     - Kommentare beginnen mit # (Rautezeichen)
#     - Zuweisung mit = (Gleichheitszeichen)
#     - Vergleich mit == (doppeltes Gleichheitszeichen)

#   0.3 Entwicklungsumgebungen
#     - PyCharm: Vollwertige IDE mit allen Funktionen
#     - VS Code: Leichtgewichtiger Editor mit Python-Erweiterung
#     - Jupyter Notebooks: Interaktive Entwicklung (nicht für Produktionscode)

